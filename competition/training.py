# contains non-ASCII characters such as ⌈⌉ and Greek letters; without forcing
# utf-8 they trigger a UnicodeEncodeError on print. These lines are pure
# defense and unrelated to the algorithm.
os.environ["PYTHONIOENCODING"] = "utf-8"
if hasattr(sys.stdout, "reconfigure"):
    sys.stdout.reconfigure(encoding="utf-8", errors="strict")
if hasattr(sys.stderr, "reconfigure"):
    sys.stderr.reconfigure(encoding="utf-8", errors="strict")

SEED              = 42                                            # Fixed random seed throughout, for reproducibility
BASE_MODEL_NAME   = "nvidia/NVIDIA-Nemotron-3-Nano-30B-A3B-BF16"  # Competition-mandated base model (must be written back to config on submission, see end)
# ⚠️ The data is NOT the official train.csv (which only has id/prompt/answer);
#    it is a third-party dataset with an extra generated_cot column -- a
#    chain-of-thought solution already generated by others. This script only
#    does the "student-side" distillation.
DATASET_PATH      = "/kaggle/input/datasets/dgxchen/nemotron-cot-tong/problem_ids_matched.csv"

# The core bet of the whole approach: treat these three types as the "hard"
# problems and train them heavily in both phases.   see §1.1
#   - cryptarithm_deduce / cryptarithm_guess: letter arithmetic, both subtypes hard
#   - equation_numeric_guess: among numeric-equation problems only the guess subtype is hard (deduce is not)
HARD_TYPES        = {"cryptarithm_deduce", "cryptarithm_guess", "equation_numeric_guess"}

# At evaluation time the host automatically appends a "put the answer inside
# \boxed{}" instruction after the problem. We append the same suffix during
# training so that "training input ≈ evaluation input". If the input
# distributions are misaligned, the model may not faithfully emit \boxed{} at
# inference.  see §1.2
PROMPT_SUFFIX     = '\nPlease put your final answer inside `\\boxed{}`. For example: `\\boxed{your answer}`'

# ── LoRA config ──────────────────────────────────────────────────────────────
LORA_RANK    = 32        # Competition rule caps rank ≤ 32; max it out: more capacity holds more of the patterns to memorize
LORA_ALPHA   = 32        # alpha/rank = 1.0, scaling factor of 1, no amplification
LORA_DROPOUT = 0.0       # Dropout off during training: distillation must replicate traces precisely, prefer overfitting to learn accurately   see §1.3
# Note in_proj/out_proj -- these are the projections of the Mamba-2 (SSM)
# blocks, which a standard Llama LoRA recipe does not have!
# q/k/v/o_proj are the Attention layers, up/down_proj are the MoE/MLP layers.
# Covering all three layer kinds mirrors the hybrid architecture.
TARGET_MODULES = ["q_proj", "k_proj", "v_proj", "o_proj",
                  "in_proj", "out_proj", "up_proj", "down_proj"] # lm_head is excluded: saves rank budget + changing the output layer is risky

# ── Phase 1 "Train": aggressive coarse training, optimizing coverage and speed ───────────────────────────────  see §7
TRAIN_LR            = 2e-4    # Large learning rate, hammer the traces hard into the LoRA
TRAIN_BATCH         = 1       # One sample per GPU (the 30B model is memory-tight)
TRAIN_GRAD_ACCUM    = 8       # Gradient accumulation of 8 steps -> effective batch = 1×8 = 8
TRAIN_EPOCHS        = 1       # One pass over the data only

# ── Phase 2 "Nudge": small-step refinement, optimizing hard-problem accuracy and stability ──────────────────────────  see §8
NUDGE_LR            = 5e-6    # Tiny learning rate, 1/40 of Phase 1, only "nudge" rather than overturn
NUDGE_BATCH         = 1
NUDGE_GRAD_ACCUM    = 8
NUDGE_EPOCHS        = 1

print("Config loaded.")
print(f"  Hard types: {HARD_TYPES}")
print(f"  Phase 1: lr={TRAIN_LR}, batch={TRAIN_BATCH}, accum={TRAIN_GRAD_ACCUM}")
print(f"  Phase 2: lr={NUDGE_LR},  batch={NUDGE_BATCH}, accum={NUDGE_GRAD_ACCUM}")


