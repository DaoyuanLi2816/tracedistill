# ════════════════════════════════════════════════════════════════════════════
#  Training infrastructure: sample construction + chat template + stratified sampler     see docs/solution.md §5, §6
# ════════════════════════════════════════════════════════════════════════════
import math, re
from collections import defaultdict
from datasets import Dataset as HFDataset
from trl import SFTTrainer, SFTConfig
from torch.utils.data import DataLoader, Sampler


def build_records(source_df):
    """Convert a dataframe into SFT samples, and return an equal-length list of type labels (for the stratified sampler).
    This function is the place in the whole file you can least afford to get wrong: a single wrong character in the format can drop answer extraction to 0 points at inference.  see §5
    """
    records, types = [], []
    for _, row in source_df.iterrows():
        cot = str(row["generated_cot"])
        # ① Data cleaning: the upstream-generated chain-of-thought may be empty / "nan" / nearly empty, so skip bad samples
        if not cot or cot == "nan" or len(cot.strip()) < 5:
            continue
        # ② Strip the \boxed{} that comes inside the chain-of-thought text: the final answer is re-assembled from the official answer, not trusting what upstream wrote
        #    (this decouples the "reasoning process" from the "final answer": process from upstream, answer from the official source)
        cot_cleaned = re.sub(r'\\boxed\{[^}]*\}', '', cot).rstrip()
        # ③ user side = problem + the forced suffix, aligning with the evaluation input
        user_content  = str(row["prompt"]) + PROMPT_SUFFIX
        # ④ assistant side = cleaned chain-of-thought + closing </think> + \boxed{} of the official answer
        #    Note: here we only add the "closing" </think>; the opening <think> is added automatically by the chat template below!
        #    Final target structure = <think>(added by template) ... chain-of-thought ... </think>(added here) \boxed{answer}(added here)
        #    This structure must match the evaluation protocol character for character, so that the model dutifully produces the same format at inference.
        asst_content  = cot_cleaned + f"\n</think>\n\\boxed{{{str(row['answer'])}}}"
        records.append({"messages": [
            {"role": "user",      "content": user_content},
            {"role": "assistant", "content": asst_content},
        ]})
        types.append(str(row["type"]))    # ⑤ Record each sample's type in parallel, to feed the stratified sampler
    return records, types


def formatting_prompts_func(example):
    """Wrap the messages in Nemotron's official chat template and convert them into plain text for training."""
    messages = example["messages"]
    # Support both single and batched input shapes, normalizing to a "list of conversations"
    if messages and isinstance(messages[0], dict):
        conversations = [messages]
    else:
        conversations = messages
    texts = []
    for conversation in conversations:
        try:
            text = tokenizer.apply_chat_template(
                conversation, tokenize=False,
                add_generation_prompt=False,   # This is training: we want the full conversation (including the answer) as the target, not to stop and wait for a continuation
                enable_thinking=True)          # Turn on thinking mode: the template will automatically insert <think> before the assistant
        except TypeError:
            # Fallback: older tokenizer versions do not recognize the enable_thinking argument
            text = tokenizer.apply_chat_template(
                conversation, tokenize=False, add_generation_prompt=False)
        texts.append(text)
    return texts


def build_stratified_index_order(labels, batch_size, seed):
    """Precompute a "type-balanced" sample order: make each effective batch contain a bit of every type as much as possible,
    avoiding the gradient-direction jitter that comes from a batch made entirely of one problem type. Core technique = dealing cards (round-robin).  see §6
    """
    # 1) Bucket sample indices by type
    by_label = defaultdict(list)
    for idx, label in enumerate(labels):
        by_label[label].append(idx)
    # 2) Shuffle each bucket internally (consumes the seed, reproducible)
    rng = random.Random(seed)
    for idx_list in by_label.values():
        rng.shuffle(idx_list)
    # 3) Prepare n_batches empty batch buckets, and shuffle the "bucket fill order" too
    n_batches = max(1, math.ceil(len(labels) / batch_size))
    batches   = [[] for _ in range(n_batches)]
    b_order   = list(range(n_batches))
    rng.shuffle(b_order)
    # 4) Deal: scatter each type's samples one by one (modulo) into the batch buckets in turn
    #    → like dealing cards, dealing one suit card by card to N players, then dealing the next suit → every bucket has every suit
    assigned  = 0
    for label in sorted(by_label.keys()):
        for idx in by_label[label]:
            batches[b_order[assigned % n_batches]].append(idx)
            assigned += 1
    # 5) Flatten all batch buckets into a one-dimensional order and return it
    order = [idx for batch in batches for idx in batch]
    if len(order) != len(labels):
        raise ValueError("Stratified order size mismatch")
    return order


class PrecomputedOrderSampler(Sampler):
    """A "take it as given" sampler: emit sample indices strictly in the given fixed order, doing no shuffling at all."""
    def __init__(self, order): self.order = list(order)
    def __iter__(self):        return iter(self.order)
    def __len__(self):         return len(self.order)


class StratifiedSFTTrainer(SFTTrainer):
    """An SFTTrainer that overrides only get_train_dataloader: feed the precomputed balanced order into the DataLoader,
    inheriting everything else from TRL's SFTTrainer (a minimal change surface, without hacking TRL's internal shuffling logic).
    """
    def __init__(self, *args, stratified_order=None, **kwargs):
        super().__init__(*args, **kwargs)
        self.stratified_order = stratified_order

    def get_train_dataloader(self):
        if self.stratified_order is None:
            return super().get_train_dataloader()     # No order given, fall back to TRL's default behavior
        if len(self.stratified_order) != len(self.train_dataset):
            raise ValueError("Stratified order length does not match train dataset")
        # Build a DataLoader ourselves; the key is that the sampler uses the "take it as given" fixed-order sampler above
        dataloader_kwargs = {
            "batch_size":         self.args.per_device_train_batch_size,
            "sampler":            PrecomputedOrderSampler(self.stratified_order),  # ← the balanced order takes effect here
            "collate_fn":         self.data_collator,
            "num_workers":        self.args.dataloader_num_workers,
            "pin_memory":         self.args.dataloader_pin_memory,
            "persistent_workers": self.args.dataloader_persistent_workers,
            "drop_last":          self.args.dataloader_drop_last,
        }
        if self.args.dataloader_num_workers > 0:
            dataloader_kwargs["prefetch_factor"] = self.args.dataloader_prefetch_factor
        return DataLoader(self.train_dataset, **dataloader_kwargs)


