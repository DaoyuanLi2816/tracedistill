# ## Dataset Split
#
# Split into two non-overlapping training sets:
# - **Epoch 1 set**: all hard problems + the easy problems not drawn for nudging
# - **Nudge set**: all hard problems + n samples of each easy type (n = the count of the smallest hard type)

# %%
# ════════════════════════════════════════════════════════════════════════════
#  Dataset split: into two "non-overlapping" training sets                see docs/solution.md §4
#    Epoch1 set (for Phase 1) = all hard problems + most easy problems
#    Nudge set  (for Phase 2) = all hard problems + n fresh samples of each easy type (type-balanced)
# ════════════════════════════════════════════════════════════════════════════
import pandas as pd, random, re
from collections import defaultdict

df       = pd.read_csv(DATASET_PATH)
train_df = df.sample(frac=1, random_state=SEED).reset_index(drop=True)   # Shuffle the whole thing; fixed seed keeps it reproducible
print(f"Total dataset: {len(train_df)} rows")
print("Full type distribution:")
print(train_df["type"].value_counts().to_string())

# Split the data into a "hard" pile and an "easy" pile according to HARD_TYPES
hard_mask = train_df["type"].isin(HARD_TYPES)
hard_df   = train_df[hard_mask].reset_index(drop=True)
easy_df   = train_df[~hard_mask].copy()  # Note: keep train_df's original row indices, we rely on them to locate rows later

# n = the count of the scarcest of the three hard types. It is the sampling
#     quota for each easy type, so that in the Nudge set the easy and hard
#     volumes are brought to the same order of magnitude and neither swamps the other.
n = int(hard_df["type"].value_counts().min())
print(f"\nHard type counts:")
print(hard_df["type"].value_counts().to_string())
print(f"\nn (samples per easy type for nudge set) = {n}")

# Randomly draw n samples from each easy type and record their original row indices in train_df (draw only easy, none of the hard)
rng = random.Random(SEED + 1)            # Use SEED+1 as a separate random source, decoupled from the shuffle above
nudge_easy_idx = set()                    # Set of easy-problem row indices "reserved for Nudge"
for etype, egroup in easy_df.groupby("type"):
    sampled = rng.sample(list(egroup.index), min(n, len(egroup)))   # Draw n from this type (take them all if fewer than n)
    nudge_easy_idx.update(sampled)
    print(f"  Easy '{etype}': sampled {len(sampled)}/{len(egroup)} for nudge set")

# ── Epoch1 set: all data minus the easy problems reserved for Nudge ───────────────────────
#    → equals: all hard problems + (all easy problems - those reserved for Nudge). Not a single hard problem is dropped.
epoch1_df = train_df[~train_df.index.isin(nudge_easy_idx)].reset_index(drop=True)

# ── Nudge set: all hard problems + the easy problems just drawn out ────────────────────────────────
nudge_easy_df = train_df[train_df.index.isin(nudge_easy_idx)]
nudge_df      = pd.concat([hard_df, nudge_easy_df]).reset_index(drop=True)

print(f"\nEpoch 1 set : {len(epoch1_df)} rows")
print("  Type distribution:", dict(sorted(epoch1_df["type"].value_counts().to_dict().items())))
print(f"\nNudge set   : {len(nudge_df)} rows")
print("  Type distribution:", dict(sorted(nudge_df["type"].value_counts().to_dict().items())))

# Guard the key invariant: the hard problems must appear in "full" in both sets (this is exactly what lets both phases train the hard problems heavily)
assert len(epoch1_df[epoch1_df["type"].isin(HARD_TYPES)]) == len(hard_df), "Hard problems missing from epoch1 set"
assert len(nudge_df[nudge_df["type"].isin(HARD_TYPES)])   == len(hard_df), "Hard problems missing from nudge set"
print("\nSanity checks passed.")


