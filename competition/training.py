# %% [markdown]
# ## Two-Phase Fine-Tuning: Train -> Nudge
#
# First train a LoRA adapter on the full dataset (minus the portion of easy
# samples held out for nudging), then nudge it with a "hard-heavy" mixed set to
# push accuracy on the hard problem types higher without forgetting the easy ones.
#
# **Phase 1 - Train:** all hard problems + the easy problems not held out for nudging.
# **Phase 2 - Nudge:** all hard problems + n samples of each easy type (n = the count of the smallest hard type).

# %%
